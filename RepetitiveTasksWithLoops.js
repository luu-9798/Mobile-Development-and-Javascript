//For loop
for (let i = 0; i <= 5; ++i) {
    if (i < 5) {
        console.log(i+1)
    } else {
        console.log('Counting completed!')
    }
}

//For loop reverse
for (let i = 5; i >= 0; --i) {
    if (i > 0) {
        console.log(i)
    } else {
        console.log('Countdown finished!')
    }
}